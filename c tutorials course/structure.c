#include <stdio.h>

int main()
{
    int a, b;
    printf("Enter the value of a : ");
    scanf("%d/n", &a);
    printf("Enter the value of b : ");
    scanf("%d/n", &b);

    if (a == b)
    {
        printf("The entered value is equal");
    }

    else
    {
        printf("The entered value is not equal");
    }

    return 0;
}
